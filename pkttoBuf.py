blc = {}
alc = {}
battery_mv = None

for line in RAW_LOG.splitlines():
    line = line.strip()
    if not line:
        continue

    data = bytes(int(b, 16) for b in line.split())
    pkt_type, count, start = struct.unpack_from("<BBH", data, 0)

    if pkt_type == 0xFF:
        break

    samples = [
        struct.unpack_from("<h", data, 4 + i * 2)[0]
        for i in range(count)
    ]

    if pkt_type == 1:
        for i, v in enumerate(samples):
            blc[start + i] = v
    elif pkt_type == 2:
        for i, v in enumerate(samples):
            alc[start + i] = v
    elif pkt_type == 3:
        battery_mv = samples[0]

